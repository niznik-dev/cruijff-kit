def generate_model_run_name(num_names=1):
    """
    Generate a random model run name using adjectives and nouns.
    
    Args:
        num_names (int): The number of names to generate. Default is 1.
        
    Returns:
        list: A list of generated names.
    """
    names = []
    for _ in range(num_names):
        adjective = random.choice(POSITIVE_ADJECTIVES)
        noun = random.choice(POSITIVE_NOUNS)
        names.append(f"{adjective}_{noun}")
    return names